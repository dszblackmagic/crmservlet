package com.crm.model;

public class Plan {
	private int id;
	private String numberId;
	private String planTime;
	private String executePlan;
	private String state;
	private String planEffect;
	
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNumberId() {
		return numberId;
	}
	public void setNumberId(String numberId) {
		this.numberId = numberId;
	}
	public String getPlanTime() {
		return planTime;
	}
	public void setPlanTime(String planTime) {
		this.planTime = planTime;
	}
	public String getExecutePlan() {
		return executePlan;
	}
	public void setExecutePlan(String executePlan) {
		this.executePlan = executePlan;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getPlanEffect() {
		return planEffect;
	}
	public void setPlanEffect(String planEffect) {
		this.planEffect = planEffect;
	}
	
	
	
}
